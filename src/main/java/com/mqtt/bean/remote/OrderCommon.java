package com.mqtt.bean.remote;

public class OrderCommon
{
    /**
     * 消息类型,此处  关机指令、重启、截图共用次消息类
     */
    public Integer msgCode ;
    
    /**
     * 消息描述
     */
    public String msgExplain;
    
    /**
     * mac地址
     */
    public String mac;
    
    /**
     * 区域码-以","作为分隔符
     */
    private String areaCode;  
    
    private String planId;
    
    
    /**
     * 标识指令下发请求
     */
    private Integer requestId;

    public Integer getMsgCode() {
        return msgCode;
    }

    public String getMsgExplain()
    {
        return msgExplain;
    }

    public void setMsgExplain(String msgExplain)
    {
        this.msgExplain = msgExplain;
    }

    public void setMsgCode(Integer msgCode)
    {
        this.msgCode = msgCode;
    }

    public String getMac()
    {
        return mac;
    }

    public void setMac(String mac)
    {
        this.mac = mac;
    }

    public String getAreaCode()
    {
        return areaCode;
    }

    public void setAreaCode(String areaCode)
    {
        this.areaCode = areaCode;
    }


    public Integer getRequestId()
    {
        return requestId;
    }

    public void setRequestId(Integer requestId)
    {
        this.requestId = requestId;
    }

    public String getPlanId()
    {
        return planId;
    }

    public void setPlanId(String planId)
    {
        this.planId = planId;
    }
    
}
